// Use the CMSIS library
#define ARM_MATH_CM4
#include "arm_math.h"
// from vowel recording, generated by MATLAB
#include "vowel_a.h"
#include "vowel_i.h"

arm_rfft_fast_instance_f32 fft_handler; // FFT structure


#define SIZE_FFT 512           /* Number of FFT points */

float32_t X[SIZE_FFT], Y[SIZE_FFT], spectrum[SIZE_FFT], Z[SIZE_FFT], ac[SIZE_FFT/2];
uint32_t pitch_index, pitch_peaks_index;
float fs = 8000; //sampling frequency
float pitch_freq;

float32_t peaks[SIZE_FFT/2];
uint32_t peaks_index[SIZE_FFT/2];
uint32_t num_peaks=0;


