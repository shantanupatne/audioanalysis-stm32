// find the index of the maximum element of an array
uint32_t max_index(float32_t* x, uint32_t length)
{
	uint32_t max_index = 0;
	float max = 0;
	for (int i = 1; i < length; i++) {
		if (x[i] > max) {
			max = x[i];
			max_index = i;
		}
	}
	return max_index;
}


