/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  /* USER CODE BEGIN 2 */
  uint32_t i;

  /* prepare for FFT: copy numbers to X */
  for (i=0; i<SIZE_FFT; i++)
  {
	  // voice recording generated by MATLAB in header file
	  X[i] = vowel_a[i];

	  spectrum[i]=0; // clear spectrum vector
  }

  // CMSIS function description: https://arm-software.github.io/CMSIS_5/DSP/html/group__RealFFT.html
  // initialize the FFT structure
  arm_rfft_fast_init_f32(&fft_handler, SIZE_FFT); // last parameter: length of the real sequence
  // perform FFT: X is the input array with all real numbers
  // Note: The FFT of a real N-point sequence has even symmetry in the frequency domain.
  // The second half of the data equals the conjugate of the first half flipped in frequency.
  // Y is the FFT of X (only first half the FFT is given, the second half can be derived as mentioned above),
  // and is packed in the following format:
  // Y = { real[0], real[N/2], real[1], imag[1], real[2], imag[2] ... real[(N/2)-1], imag[(N/2)-1 }
  arm_rfft_fast_f32(&fft_handler, X, Y, 0); // last parameter: 0 forward transform, 1 inverse transform


  for (i=0; i<SIZE_FFT; i=i+2)
  {
	  spectrum[i] = Y[i] * Y[i] + Y[i+1] * Y[i+1]; // compute spectrum: amplitude squared = real^2 + imaginary^2

  }
  spectrum[0] =  Y[0] * Y[0]; // correct for FFT[0]
  spectrum[1] = Y[1] * Y[1];  // correct for FFT[N/2]
  // imaginary parts of spectrum are already set to 0 from initialization

  arm_rfft_fast_f32(&fft_handler, spectrum, Z, 1); // inverse transform, Z is real-valued

  // autocorrelation function is inverse FFT of spectrum
  for (i=0; i<SIZE_FFT/2; i++) // symmetric, keep the 1st half
  {
	  ac[i] = Z[i];
  }

  // find all peaks of the autocorrelation function
  uint8_t direction = 0; // 0 is going up, 1 is going down
  for (i=1; i<SIZE_FFT/2; i++)
  {
	  if (ac[i] < ac[i-1] && direction == 0) {
		  peaks[num_peaks] = ac[i-1];
		  peaks_index[num_peaks] = i-1;
		  num_peaks = num_peaks + 1;
		  direction = 1;

	  } else if (ac[i] > ac[i-1] && direction == 1) {
		  direction = 0;
	  }
  }

  if (direction == 0) {
	  // count last element in the array as a peak
	  peaks[num_peaks] = ac[SIZE_FFT/2-1];
	  peaks_index[num_peaks] = SIZE_FFT/2-1;
	  num_peaks = num_peaks + 1;
  }

  // search for the highest peak of the auto correlation function
  // ignore self correlation, which is the first element auto_corr(1), if it is a peak
  uint8_t index_offset;
  if (peaks_index[0] == 0) {
	  index_offset = 1; // search max peak from index 1 of peaks array, ignore self correlation peak (index 0)
  } else {
	  index_offset = 0; // search max peak from index 0 of peaks array
  }

  // search max value of an array, return the index
  pitch_peaks_index = max_index(&(peaks[index_offset]), num_peaks-index_offset);

  pitch_index = peaks_index[pitch_peaks_index+index_offset]; // correct index if search from index 1

  pitch_freq = 1/(pitch_index/fs);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

